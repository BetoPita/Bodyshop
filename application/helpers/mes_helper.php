<?php
function getMes($mes){
	if($mes == "Enero"){
		return '01';
	}
	if($mes == "Febrero"){
		return '02';
	}
	if($mes == "Marzo"){
		return '03';
	}
	if($mes == "Abril"){
		return '04';
	}
	if($mes == "Mayo"){
		return '05';
	}
	if($mes == "Junio"){
		return '06';
	}
	if($mes == "Julio"){
		return '07';
	}
	if($mes == "Agosto"){
		return '08';
	}
	if($mes == "Septiembre"){
		return '09';
	}
	if($mes == "Octubre"){
		return '10';
	}
	if($mes == "Noviembre"){
		return '11';
	}
	if($mes == "Diciembre"){
		return '12';
	}
	
}
?>